    public void emprestar() throws ClassNotFoundException{
           String aluno = jComboBox2.getSelectedItem().toString();
           String livro = jComboBox1.getSelectedItem().toString();
           String data = jTextField1.getText();        
           try{
               Class.forName("com.mysql.cj.jdbc.Driver");
               Connection con = DriverManager.getConnection("jdbc:mysql://localhost/biblioteca", "root", "");              
               String sql = "INSERT INTO emprestimo (aluno, livro,data) VALUES (?,?,?)";
               PreparedStatement statement = con.prepareStatement(sql);
               statement.setString(1, aluno);
               statement.setString(2, livro);
               statement.setString(3, data);
               statement.executeUpdate();

            con.close();
           }catch(ClassNotFoundException | SQLException ex){
               ex.printStackTrace();
           }         
    }
       
