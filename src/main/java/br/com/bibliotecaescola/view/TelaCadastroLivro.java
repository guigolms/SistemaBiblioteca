    public void preenchercombo(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con =DriverManager.getConnection("jdbc:mysql://localhost/biblioteca","root","");
            Statement st = con.createStatement();
            ResultSet res = st.executeQuery("select generos from generos");
            Vector vector = new Vector();
         while(res.next()){
            vector.addElement(new Item(res.getString("generos")));
        }
            jComboBox1.setModel(new DefaultComboBoxModel(vector));
        }catch(Exception e){
          e.printStackTrace();
        }

    }
    
     private void inserirDados() throws SQLException {
        String titulo = jTextField1.getText(); 
        String genero = (String) jComboBox1.getSelectedItem().toString();
        String autor = jTextField3.getText();
        String editora = jTextField4.getText();
               

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/biblioteca", "root", "");

            String sql = "INSERT INTO livros (titulo, genero, autor, editora) VALUES (?, ?, ?, ?)";
            PreparedStatement statement = con.prepareStatement(sql);
            statement.setString(1, titulo);
            statement.setString(2, genero);
            statement.setString(3, autor);
            statement.setString(4, editora);
            statement.executeUpdate();

            con.close();
        } catch (ClassNotFoundException | SQLException ex) {
            ex.printStackTrace();
        }
    }
    
